$(document).ready(function () {
  function updateSvgVisibility() {
    $('.selectable-option').each(function () {
      const $option = $(this);
      const isSelected = $option.hasClass('selected');

      const $defaultSvg = $option.find('.default-svg');
      const $check1 = $option.find('.svg_check_type1');
      const $check2 = $option.find('.svg_check_type2');

      if (isSelected) {
        // Show only one checkmark depending on which one exists
        if ($check1.length) {
          $check1.show();
          $check2.hide();
        } else if ($check2.length) {
          $check2.show();
          $check1.hide();
        } else {
          $check1.hide();
          $check2.hide();
        }
        $defaultSvg.hide();
      } else {
        $defaultSvg.show();
        $check1.hide();
        $check2.hide();
      }
    });
  }

  $('.selectable-option').on('click', function () {
    $('.selectable-option').removeClass('selected active');

    $(this).addClass('selected active');

    const $input = $(this).find('input[type="radio"]');
    if ($input.length) {
      $input.prop('checked', true);
    }

    updateSvgVisibility();
  });

  $('.more-package-select').on('click', function () {
    $('.selectable-option').removeClass('selected active');
    // Hide checkmarks
    $('.svg_check_type1, .svg_check_type2').hide();
    
    $('.default-svg').show();
  });

  updateSvgVisibility();
});

</script>

